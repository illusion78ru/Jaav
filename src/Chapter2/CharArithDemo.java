package Chapter2;

public class CharArithDemo {
    public static void main(String args[]){
        char ch;
        ch='x';
        System.out.println("ch содержит: " + ch);
        ch++;
        System.out.println("Теперь ch содержит " + ch);
        ch=90;
        System.out.println("Теперь ch содержит " + ch);
    }
}
